public class AsciiCode {
    public static void main(String[] args) {
        char value2 = 'a';
        int value = 'a';
        System.out.println("The ASCII code is: ");
        for(int i = 1;i <=26;i++){
            System.out.println(value2 + " = " + value + " ");
            value2++;
            value++;
        }
        char value3 = 'A';
        int value4 = 'A';
        System.out.println("The ASCII code is: ");
        for(int i = 1;i <=26;i++){
            System.out.println(value3 + " = " + value4 + " ");
            value3++;
            value4++;
        }
    }
}
